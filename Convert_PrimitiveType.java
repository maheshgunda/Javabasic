package Assignments;

public class Convert_PrimitiveType {

	public static void main(String[] args) {

		// Int_to_Byte
		int i = 1000;

		byte convert_byte = (byte) i;
		System.out.println(convert_byte);

		// double to int
		double d = 100.3;

		int convert_int = (int) d;
		System.out.println(convert_int);
		
		
		// byte to double
		
		byte b=100;
		double convert_double= b;
		System.out.println(convert_double);
		
		
		//Int to long
		
		int a=100;
		
		long convert_long= (long)a;
		System.out.println(convert_long);

	}

}
